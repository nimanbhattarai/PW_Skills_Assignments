package Program;

public class PW_Ass_1_QST_5 {
    public static void main(String[] args) {
        int a=9;
        int b=8;
        int swap;
        System.out.println("The first number before swapping is : "+a);
        System.out.println("The second number before swapping is: "+b);
        swap=a;
        a=b;
        b=swap;
        System.out.println("The first number after swapping is: "+a);
        System.out.println("The second after swapping is: "+b);
    }
}
